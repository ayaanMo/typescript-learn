{
    // 布尔类型
    let isDone: boolean = false;
    // 数值类型
    let decLiteral: number = 6;
    // 字符串类型
    let myName: string = 'Tom';
    // 空值
    function alertName(): void {
        alert('My name is Tom');
    }
    // Null和Undefined
    // 与void的区别是，undefined和null是所有类型的子类型
    let u: undefined = undefined;
    let n: null = null;
}
